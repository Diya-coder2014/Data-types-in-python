print("TypeCasting")

name = "penguin"
age = 12
weight = 38.5
is_student = True

print(name)
print(age)
print(weight)
print(is_student)

print("Data type of 'name':", type(name))
print("Data type of 'age':", type(age))
print("Data type of 'weight':", type(weight))
print("Data type of 'is_student':", type(is_student))

new_age = str(age)
print(new_age)
print("Data type of 'new_age':",new_age)

new_is_student = int(weight)
print(new_is_student)
print("Data type of 'new_age':",new_is_student)